# hacer programa que compare 2 numeros y me diga si son iguales
# o cual es mayor 

num_1= float(input("ingrese un numero: "))
num_2= float(input("ingrese otro numero: "))

if num_1 == num_2:
    print("son iguales")
    
    
elif num_1 >= num_2:
    print("el mayor es", num_1)
    
    
elif num_1 <= num_2:
    print("el mayor es", num_2)